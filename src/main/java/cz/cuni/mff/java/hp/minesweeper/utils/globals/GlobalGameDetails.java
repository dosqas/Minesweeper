package cz.cuni.mff.java.hp.minesweeper.utils.globals;

/**
 * The `GlobalGameDetails` class is a utility class that stores global game details.
 * It contains the height, width, mine count, time limit, difficulty, power-up, and final time of the game.
 */
public class GlobalGameDetails {
    public static int HEIGHT;
    public static int WIDTH;
    public static int MINE_COUNT;
    public static int TIME_LIMIT = -1;
    public static int DIFFICULTY = 0;
    public static int POWER_UP = 0;
    public static int FINAL_TIME;
}
